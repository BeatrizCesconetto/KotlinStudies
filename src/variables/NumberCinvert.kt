package variables

fun main() {

    val i1 = 10
    val d1 = i1.toDouble()
    println(d1)

    val l1 = i1.toLong()
    println(l1)

    val d2 = 10.34
    val i2 = d2.toInt()
    println(i2)

    val i3 = 2000
    val b1 = i3.toByte()
    println(b1)

    val b2 = -48
    val i4 = b2.toInt()
    println(i4)

    //LEMBRAR DE CONFERIR SE O VALOR QUE FOR CONVERTER CABE NO TIPO "NOVO"
}